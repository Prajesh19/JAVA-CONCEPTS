public class relationaloperator {
    public static void main(String[] args) {
        int a =9;
        int b = 12;
        int c = a & b;
        System.out.println("a & b is: "+ c);
        c = a|b;
        System.out.println("a | b is: "+ c);
        c = a ^ b;
        System.out.println("a ^ b is: "+ c);
         c =~a;
        System.out.println("~a  is: "+c);
    }
}
